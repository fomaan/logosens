/* GALLERY LIGHTBOX */
const galleryItems = Array.from(document.querySelectorAll('#galleryGrid .gallery-item'));
const galleryData = galleryItems.map(item => ({
  type: item.getAttribute('data-type') || 'image',
  src: item.getAttribute('data-src') || item.querySelector('img').src
}));
const lightbox = document.getElementById('lightbox');
const lightboxImg = document.getElementById('lightboxImg');
const lightboxVideo = document.getElementById('lightboxVideo');
const lightboxClose = document.getElementById('lightboxClose');
const lightboxPrev = document.getElementById('lightboxPrev');
const lightboxNext = document.getElementById('lightboxNext');
let lightboxIndex = 0;
function renderLightbox(){
  const item = galleryData[lightboxIndex];
  if(item.type === 'video'){
    lightboxVideo.src = item.src;
    lightboxVideo.style.display = 'block';
    lightboxImg.style.display = 'none';
    lightboxVideo.currentTime = 0;
    lightboxVideo.play().catch(()=>{});
  } else {
    lightboxVideo.pause();
    lightboxVideo.removeAttribute('src');
    lightboxVideo.style.display = 'none';
    lightboxImg.style.display = 'block';
    lightboxImg.src = item.src;
  }
}
function openLightbox(index){ lightboxIndex = index; renderLightbox(); lightbox.classList.add('open'); }
function closeLightbox(){ lightbox.classList.remove('open'); lightboxVideo.pause(); }
function showNext(){ lightboxIndex = (lightboxIndex + 1) % galleryData.length; renderLightbox(); }
function showPrev(){ lightboxIndex = (lightboxIndex - 1 + galleryData.length) % galleryData.length; renderLightbox(); }
galleryItems.forEach((item, index)=>{ item.addEventListener('click', ()=> openLightbox(index)); });
lightboxClose.addEventListener('click', closeLightbox);
lightboxNext.addEventListener('click', showNext);
lightboxPrev.addEventListener('click', showPrev);
lightbox.addEventListener('click', (e)=>{ if(e.target === lightbox) closeLightbox(); });
document.addEventListener('keydown', (e)=>{
  if(!lightbox.classList.contains('open')) return;
  if(e.key === 'Escape') closeLightbox();
  if(e.key === 'ArrowRight') showNext();
  if(e.key === 'ArrowLeft') showPrev();
});

/* SWIPE NAVIGATION (mobile) */
let touchStartX = 0;
lightbox.addEventListener('touchstart', (e)=>{ touchStartX = e.changedTouches[0].screenX; }, {passive:true});
lightbox.addEventListener('touchend', (e)=>{
  const touchEndX = e.changedTouches[0].screenX;
  const diff = touchEndX - touchStartX;
  if(Math.abs(diff) > 40){
    if(diff < 0) showNext(); else showPrev();
  }
}, {passive:true});

